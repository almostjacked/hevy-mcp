import { readFileSync, writeFileSync } from "node:fs";

const pkg = JSON.parse(readFileSync(new URL("../package.json", import.meta.url)));

const manifestUrl = new URL("../mcpb/manifest.json", import.meta.url);
const manifest = JSON.parse(readFileSync(manifestUrl));
manifest.version = pkg.version;
writeFileSync(manifestUrl, JSON.stringify(manifest, null, 2) + "\n");

const versionTs = `// Generated by scripts/sync-versions.mjs from package.json — do not edit.\nexport const VERSION = "${pkg.version}";\n`;
writeFileSync(new URL("../src/version.ts", import.meta.url), versionTs);

const serverJsonUrl = new URL("../server.json", import.meta.url);
const serverJson = JSON.parse(readFileSync(serverJsonUrl));
serverJson.version = pkg.version;
for (const p of serverJson.packages ?? []) {
  if (p.identifier === pkg.name) p.version = pkg.version;
}
writeFileSync(serverJsonUrl, JSON.stringify(serverJson, null, 2) + "\n");

console.log(`hevy-mcp manifest + src/version.ts + server.json -> ${pkg.version}`);
